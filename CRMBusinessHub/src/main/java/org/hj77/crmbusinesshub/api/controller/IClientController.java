package org.hj77.crmbusinesshub.api.controller;

/**
 * Интерфейс контроллера клиентов, определяющий операции для взаимодействия с клиентами.
 */
public interface IClientController {
    /**
     * Получает информацию о клиенте по уникальному идентификатору.
     *
     * @param str Уникальный идентификатор клиента
     * @return Строка с информацией о клиенте
     */
    String getClient(String str);

    /**
     * Осуществляет поиск клиента по различным критериям (например, ФИО, номеру телефона).
     *
     * @param str Критерий поиска (например, имя или номер телефона)
     * @return Строка с результатами поиска
     */
    String findClient(String str);

    /**
     * Предоставляет список последних операций клиента.
     *
     * @param str Уникальный идентификатор клиента
     * @return Список последних операций в виде строки
     */
    String lastOperations(String str);

    /**
     * Сохраняет информацию о новом клиенте.
     *
     * @param str Данные клиента в виде строки (например, JSON-представление)
     * @return Строка-подтверждение успешности операции
     */
    String saveClient(String str);

    /**
     * Определяет уровень лояльности клиента.
     *
     * @param str Уникальный идентификатор клиента
     * @return Уровень лояльности клиента (например, "Bronze", "Silver", "Gold")
     */
    String clientLevel(String str);

    /**
     * Отображает историю выплат кредита клиента.
     *
     * @param str Уникальный идентификатор клиента
     * @return Строка с историей платежей по кредитам
     */
    String clientLoanPayment(String str);

}
